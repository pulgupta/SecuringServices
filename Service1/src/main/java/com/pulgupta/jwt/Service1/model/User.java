package com.pulgupta.jwt.Service1.model;

public class User {
    String id;
    String name;
    String companyName;
    String city;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getCompanyName() {
        return companyName;
    }

    public String getCity() {
        return city;
    }
}
